package projecto.CP2;

public class TotalAnos {
    int ano;
    float totalCusto;

    public TotalAnos(int ano, float totalCusto) {
        this.ano = ano;
        this.totalCusto = totalCusto;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public float getTotalCusto() {
        return totalCusto;
    }

    public void setTotalCusto(float totalCusto) {
        this.totalCusto = totalCusto;
    }
}
